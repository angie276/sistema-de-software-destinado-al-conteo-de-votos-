    private void btnGuardarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarRegistroActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String sigla = txtSigla.getText();
        String logo = txtLogo.getText();
        String legal = txtLegal.getText();
        PartidoPolitico p1 = new PartidoPolitico(nombre, sigla, logo, legal);
        
        if(p1.datosCompletados()){
            partidos.agregarPartido(p1);
            JOptionPane.showMessageDialog(this, "Registrado"+"\n"+
                    "ID: "+p1.getId());
            
            txtNombre.setText("");
            txtSigla.setText("");
            txtLogo.setText("");
            txtLegal.setText("");
            
        }else{
            JOptionPane.showMessageDialog(this, "Datos incompletos");
        }
        
        
    }//GEN-LAST:event_btnGuardarRegistroActionPerformed

