    public void verLogo(String rutaL){
        try { //permite que no se detenga de manra brusca si algo dale mal
            URL url = new URL(rutaL);   
            ImageIcon icon = new ImageIcon(url); 
            
            Image logo = icon.getImage();
            Image medidas = logo.getScaledInstance(50, 50, Image.SCALE_SMOOTH);
               
            lbLogo.setIcon(icon); 
            lbLogo.setText("");  
        } catch (MalformedURLException error) {
            lbLogo.setText("No se pudo cargar el logo");
        }         
        
    }
    
