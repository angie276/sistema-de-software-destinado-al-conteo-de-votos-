    private void txtIDPartidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIDPartidoActionPerformed
        int id = Integer.parseInt(txtIDPartido.getText());
        PartidoPolitico a = partidos.buscarPartido(id);
        if (a != null) {
            txtIDPartido.setEnabled(false); 

            txtNombreModificar.setEnabled(true);
            txtLogoModificar.setEnabled(true);
            txtSiglaModificar.setEnabled(true);
            txtLegalModificar.setEnabled(true);

            txtNombreModificar.setText(a.getNombrePartido());
            txtSiglaModificar.setText(a.getSigla());
            txtLogoModificar.setText(a.getLogo());
            txtLegalModificar.setText(a.getRepresentanteLegal());

            modificar = true; 
            btnModificar.setText("Guardar cambios");
            btnModificar.setEnabled(true);
         }else{
             JOptionPane.showMessageDialog(this, "Partido no registrado");
         }
    }//GEN-LAST:event_txtIDPartidoActionPerformed

