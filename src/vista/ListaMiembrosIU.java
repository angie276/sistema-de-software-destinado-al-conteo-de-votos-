    public void rellenarDatosTabla(){
        for (int i = 0; i < miembros.longitud(); i++) {
            MiembroMesa m = miembros.iesimo(i);
            int ID = m.getId();
            String Nombre = m.getNombres();
            String Apellido = m.getApellidos();
            String DNI = String.valueOf(m.getDNI());
            String Tipo = m.getTipoMiembro();
            String ME = m.getMesa().verEnTabla();
            String E = m.getEleccion().verInfo();
            
            

            String[] fila = new String[7];
            fila[0] = String.valueOf(ID);
            fila[1] = Nombre;
            fila[2] = Apellido;
            fila[3] = DNI;
            fila[4] = Tipo;
            fila[5] = ME;
            fila[6] = E;

            this.tablaMiembros.addRow(fila);        
        }
    }
    
